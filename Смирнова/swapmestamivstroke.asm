MOV @STR, R0
ADD 1, R0           ; начинаем со второго символа

LOOP:
MOVB (R0), R2       ; R2 = текущий символ
CMPB 2E, R2         ; конец строки?
JZ END
CMPB 58, R2         ; сравниваем с 'X' (58 hex)
JZ SWAP             ; нашли X
ADD 1, R0           ; следующий символ
JMP LOOP

SWAP:
; R0 = адрес X
; левый сосед = R0-1, правый сосед = R0+1
MOV R0, R1
SUB 1, R1           ; R1 = адрес левого соседа
MOVB (R1), R2       ; R2 = левый сосед

MOV R0, R3
ADD 1, R3           ; R3 = адрес правого соседа
MOVB (R3), R0       ; R0 = правый сосед (временно)

MOVB R0, (R1)       ; записываем правого на место левого
MOVB R2, (R3)       ; записываем левого на место правого

END:
STOP

STR:
DATA "abXcd."


